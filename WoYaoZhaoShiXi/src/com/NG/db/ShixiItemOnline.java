package com.NG.db;

public class ShixiItemOnline {
	private int item_id;
	private String title;
	private String source;
	private String time;
	private String source_url;
	private String text_body;
	private int is_clicked;
	private int is_collected;
	
	
	public int getItem_id() {
		return item_id;
	}
	public void setItem_id(int item_id) {
		this.item_id = item_id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getSource_url() {
		return source_url;
	}
	public void setSource_url(String source_url) {
		this.source_url = source_url;
	}
	

	public String getText_body() {
		return text_body;
	}
	public void setText_body(String text_body) {
		this.text_body = text_body;
	}
	public int getIs_clicked() {
		return is_clicked;
	}
	public void setIs_clicked(int is_clicked) {
		this.is_clicked = is_clicked;
	}
	public int getIs_collected() {
		return is_collected;
	}
	public void setIs_collected(int is_collected) {
		this.is_collected = is_collected;
	}
	
	
	

}
